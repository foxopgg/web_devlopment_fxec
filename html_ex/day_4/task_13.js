// Using + for concatenation
let firstName = "John";
let lastName = "Doe";
let age = 25;

let sentenceUsingPlus = "My name is " + firstName + " " + lastName + " and I am " + age + " years old.";
console.log(sentenceUsingPlus);

// Using template literals
let sentenceUsingTemplate = `My name is ${firstName} ${lastName} and I am ${age} years old.`;
console.log(sentenceUsingTemplate);